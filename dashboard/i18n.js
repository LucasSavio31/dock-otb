let currentLang = localStorage.getItem('otb-lang') || 'pt';

function t(key) {
  return (I18N[currentLang] && I18N[currentLang][key]) || (I18N['pt'][key]) || key;
}

function setLanguage(lang) {
  if (!I18N[lang]) lang = 'pt';
  currentLang = lang;
  localStorage.setItem('otb-lang', lang);

  const dict = I18N[lang];

  // Update lang button label
  const btnLang = document.getElementById('btnLang');
  if (btnLang) btnLang.textContent = { pt: 'PT-BR', en: 'EN', es: 'ES' }[lang];

  // Translate all [data-i18n] elements (textContent)
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.dataset.i18n;
    const val = dict[key] || I18N['pt'][key] || key;
    if (val.includes('&') || val.includes('<')) {
      el.innerHTML = val;
    } else {
      el.textContent = val;
    }
  });

  // Translate placeholders [data-i18n-ph]
  document.querySelectorAll('[data-i18n-ph]').forEach(el => {
    const key = el.dataset.i18nPh;
    el.placeholder = dict[key] || I18N['pt'][key] || key;
  });

  // Translate title attributes [data-i18n-title]
  document.querySelectorAll('[data-i18n-title]').forEach(el => {
    const key = el.dataset.i18nTitle;
    el.title = dict[key] || I18N['pt'][key] || key;
  });

  // Re-render dynamic JS content after language change
  if (typeof onLanguageChange === 'function') onLanguageChange(lang);
}
